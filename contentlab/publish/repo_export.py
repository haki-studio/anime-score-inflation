import shutil
from pathlib import Path

# never copy caches / cruft into the public repo
_IGNORE = shutil.ignore_patterns("__pycache__", "*.pyc", ".DS_Store", ".env", ".env.*")

# files copied flat from the post dir to the repo root (skipped if absent)
_POST_FILES = ("run.py", "config.py", "narrative.html", "excerpt.md")

# output/ is generated by run.py and is never committed to a public article repo
_GITIGNORE = "__pycache__/\n*.pyc\n.DS_Store\n.env\n.env.*\noutput/\n"

