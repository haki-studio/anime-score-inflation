def export_article(post_dir, contentlab_dir, dest, *, title, summary="",
                   github_url="", article_url=None, include_data=True,
                   holder="Haki Studios", year=2026, force=False):
    """Write a self-contained public repo for one post into `dest`.

    post_dir       : the post's source dir (posts/<slug>/)
    contentlab_dir : the toolkit package dir to vendor (contentlab/)
    dest           : target dir (repos/<slug>/); overwritten if force=True
    Returns the dest Path. Pure filesystem copy; does not touch git.
    """
    post_dir = Path(post_dir)
    contentlab_dir = Path(contentlab_dir)
    dest = Path(dest)

    if dest.exists():
        if not force:
            raise FileExistsError(f"{dest} already exists; pass force=True to overwrite")
        # clear the contents but PRESERVE the repo's own git history, so re-exports
        # update files in place and can be re-committed rather than losing the repo.
        for child in dest.iterdir():
            if child.name in (".git",):
                continue
            shutil.rmtree(child) if child.is_dir() else child.unlink()
    else:
        dest.mkdir(parents=True)

    # 1) the post, flattened to the repo root
    for name in _POST_FILES:
        src = post_dir / name
        if src.exists():
            shutil.copy2(src, dest / name)

    # 2) the cached data (the "data is open" promise). Generated output/ is NOT
    #    shipped - it is reproducible via run.py and is gitignored (public-repo rule).
    if include_data and (post_dir / "data").is_dir():
        shutil.copytree(post_dir / "data", dest / "data", ignore=_IGNORE)

    # 3) vendored toolkit so the repo runs with no install
    shutil.copytree(contentlab_dir, dest / "contentlab", ignore=_IGNORE)

    # 4) repo metadata
    (dest / "README.md").write_text(_readme(
        title=title, summary=summary, github_url=github_url,
        article_url=article_url, include_data=include_data), encoding="utf-8")
    (dest / "LICENSE").write_text(_MIT.format(year=year, holder=holder), encoding="utf-8")
    (dest / ".gitignore").write_text(_GITIGNORE, encoding="utf-8")

    return dest
